-- Create timezones table
CREATE TABLE IF NOT EXISTS public.timezones (
    id SERIAL PRIMARY KEY,
    tz_identifier VARCHAR(100) NOT NULL UNIQUE,
    display_name VARCHAR(150) NOT NULL,
    utc_offset VARCHAR(10) NOT NULL,
    display_order INT DEFAULT 0,
    is_active BOOLEAN DEFAULT true,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);

-- Enable RLS
ALTER TABLE public.timezones ENABLE ROW LEVEL SECURITY;

-- Create policy for read access (public)
CREATE POLICY "Allow public read access" ON public.timezones
    FOR SELECT USING (true);

